        <p>ESTA申請時には、ICチップ付きで有効期限内のパスポートが必須です。 申請フォームには、パスポート番号や発行国、発行日、有効期限、氏名、生年月日といったパスポート情報を正確に入力する必要があります。これらの情報は、パスポートに記載されている内容と完全に一致していることが重要なため、申請時は必ず手元にパスポートを準備しておきましょう。</p>
